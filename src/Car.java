public class Car {
    private String mark, model, color, status;
    private int cost, mileage;

    Car(String mark, String model, String color, String status, int cost, int mileage){
        this.mark = mark;
        this.model = model;
        this.color = color;
        this.cost = cost;
        this.status = status;
        this.mileage = mileage;
    }
}
